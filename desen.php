<h2 class="text-center">DESENVOLVEDORES</h2>
	<p>Os fundadores da Blizzard Entertainment são jogadores de cards 
	colecionáveis e adeptos desta modalidade desde quando a empresa era
 	conhecida como Silicon & Synapse. De acordo com a empresa, lançar 
	um jogo estratégico digital foi algo natural e para isso foi criado
 	um time de desenvolvimento pequeno para fazer algo diferente 
 	dos outros jogos já lançados pela desenvolvedora.</p>

 	<img class="img-responsive center-block" src="desenvolvedores2.jpg" alt="" width="750" height="400">

 	<p>Hearthstone: Heroes of Warcraft foi desenvolvido de forma que 
 	seja fácil de assimilar, mas ofereça também profundidade e uma 
 	de suas principais inspirações é o jogo Magic: The Gathering, 
 	um jogo de cartas colecionáveis (TCG, Trading Card Game) criado
 	por Richard Garfield, no qual os jogadores utilizam um baralho
 	de cartas construído de acordo com o seu modo individual de 
 	jogo para tentar vencer o adversário.</p>

 	<img class="img-responsive center-block" src="desenvolvedores.jpg" alt="" width="750" height="400">

 	<p>Contudo, para muitos a experiencia com Magic pode ter sido 
 	apresentada como um jogo aonde o que importa é a estratégia e 
 	isso pode afastar muitos jogadores. Com Hearthstone o objetivo
 	é diferente. Haverá um visual amigável e personagens que
 	possuem uma história rica por trás.</p>