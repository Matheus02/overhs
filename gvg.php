<h2 class="text-center">GOBLINS VS GNOMOS</h2>

	<p>Hearthstone: Heroes of Warcraft é atualmente um dos jogos mais populares da 
	Blizzard. Contanto com cerca de 20 milhões de jogadores, o game de cartas para
	 PC, Mac e iPad é um sucesso instantâneo. A atualização “Goblins vs Gnomos”, 
	 prevista para hoje (08/12/2014), irá trazer 120 cartas novas, rebalancear algumas
	  cartas dos principais decks atuais e possibilitar novos meios de ativação 
	  para “Grito de Guerra” e “Último Suspiro”. Confira o que esperar dessa nova
	   expansão.</p>

