	   	<h3 class="text-center">NOVAS CARTAS</h3>

	<p>A foco da expansão “Goblins vs Gnomos” será a adição de cartas temáticas 
	entre as duas raças, goblins e gnomos. Com ênfase nas invenções malucas de ambas,
	 a expansão irá trazer mecanóides (robôs) que adicionam cartas de baixo custo de
	  mana ao serem lançados no campo de batalha.</p>

	<p>Chamadas de “peças sobressalentes”, essas cartas com apenas um de mana, podem
	 ser usadas para aumentar os status dos seus lacaios. Haverá também cartas que
	  evocam robôs grandes que ao serem destruídos, irão evocar, graças ao “Último 
	  Suspiro”, lacaios “menores”, garantindo alguma reação depois de magias com 
	  dano em área.</p>

	<p>Serão 49 lacaios neutros que poderão ser usados por qualquer classe. 
	Sete peças sobressalentes e oito novas cartas para cada classe.</p>

	<p>Uma das principais novidades, para quem possui decks com muitos lacaios
	 com “Último Suspiro”, é a carta da classe Caçador, chamada “Fingir de morto”.
	  Para quem não conhece, o “Último Suspiro” ativa a habilidade da carta
	   apenas quando o lacaio morre.</p>

	<p>Mas com a carta “Fingir de morto” é possível usar o recurso, sem perder
	 o lacaio. EstA carta deverá ser bastante usada, já que a expansão 
	 “Maldição de Naxxramas” (lançada em julho de 2014) trouxe muitas cartas 
	 com “Último Suspiro” para o “meta game”.</p>

	<p>Porém, a expansão também trará complicações para os amantes de decks
	 com “Último Suspiro”. Um exemplo claro é a carta do lacaio “Purificador Escarlate”,
	  exclusiva do Paladino. Ao ser jogado no campo, este lacaio irá causar 
	  dois de dano a todos os lacaios com “Último Suspiro”. Perfeito para deixar
	   alguns lacaios dentro do raio de ação de magias, mas também é uma carta ideal 
	   para a ativação do Ovo Nerubiano.</p>

		<h3 class="text-center">COMO ADQUIRIR AS NOVAS CARTAS</h3>

	<p>As cartas da nova expansão poderão ser adquiridas através dos pacotes de cartas
	 que podem ser comprados com dinheiro real, conquistados no modo arena, ou comprados
	  com o dinheiro virtual do jogo. Além disso, o jogador também poderá “criar” essas 
	  cartas através do “Pó Arcano”.</p>

	<p>O meio mais rápido de adquirir novos cartas sem gastar dinheiro real é aperfeiçoando
	 suas habilidades no Modo Arena. Neste modo, você encara outros jogadores usando decks
	  aleatórios. A partir de cinco vitórias o jogador já consegue recuperar mais do que
	   foi investido em ouro para acessar o modo e ainda ganha pacotes e premiações extras.</p>

		<h3 class="text-center">INVASÃO DAS NOVAS CARTAS NO MODO ARENA</h3>

	<p>A preparação para a expansão “Goblins vs Gnomos” começou nesta quinta-feira. 
	Um pequeno update foi lançado e já é possível conferir os novos decks no modo Arena.
	 Contudo, ainda não é possível adquirir as novas cartas, apenas jogar com elas neste
	  modo.</p>

	<p>Também neste pequeno update veio algumas formas de rebalanceamento, os temíveis “nerfs”. 
	A carta “Sinalizador”, da classe caçador, passará a custar dois de mana. O Leiloeiro de 
	Geringontzan, bastante utilizado pela classe Ladino, também ganhou um “nerf” e agora passa
	 a custar seis de mana. O rebalanceamento também atingiu a classe Bruxo, o “Fogo d’Alma”,
	  que não custava nada, agora custa um de mana.</p>
