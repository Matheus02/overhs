<h2 class="text-center">O QUE É HEARTHSTONE?</h2>
<p>Hearthstone é um jogo de cards estratégico on-line no estilo TCG (Trade Card Game) desenvolvido e 
publicado pela empresa Blizzard Entertainment. É o primeiro jogo da empresa gratuito e também o
 primeiro a ser lançado para plataformas móveis. No jogo os jogadores constroem decks a partir de 
 heróis que representam nove classes do universo de Warcraft e trocam turnos jogando cards de seus 
 decks, como lacaios, armas, feitiços e utilizando porder heróico.</p>
