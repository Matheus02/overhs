  	<p>A necrópole ancestral de Naxxramas, base de operações 
  	do porderoso lich Kel’Thuzad e sua hoste de mortos-vivos
  	 pestilentos, chega a Hearthstone com uma Aventura JxA 
  	 individual na qual qualquer um pode embarcar! Avance por
  	  uma das cinco alas da masmorra e ganhe novos cards para
  	   a sua coleção derrotando os chefes icônicos que você
  	    encontrará ao longo do caminho — a aranha gigante
  	     Maexxna, o horror fúngico Repugnaz e a abominação
  	      claudicante Retalhoso são apenas algumas das 
  	      tribulações que esperam pelo seu desafio. Cada 
  	      chefe tem cards e Poderes Heroicos exclusivos
  	       para usar contra até mesmo o mais experiente
  	        dos aventureiros carteadores!</p>

  	<p>Ao avançar pela Maldição de Naxxramas, você receberá 
  	cartas inéditas como recompensa. Ao todo 30 novas cartas 
  	estarão disponíveis. A medida que avançar na maldição, 
  	você ganha uma carta lendária por ala concluída! </p>
	
	<p>Depois do Distrito dos Aracnídeos, as próximas quatro 
	alas disponíveis aos jogadores poderão ser compradas por
	 700 de ouro por ala ou R$15,90 por ala. Jogadores podem
	  conseguir o ouro do jogo para desbloquear a próxima 
	  ala de Naxxramas completando as missões ou sendo 
	  vitorioso na Arena, ou eles podem desbloquear a próxima
	   ala de Naxxramas usando dinheiro real.</p>

	<p>Um dos chefes que você deve enfrentar na Ala da Peste 
	é o diabólico mestre do caldeirão pestilento Heigan, o 
	Sujo! Seu feitiço Erupção testa o rebolado dos seus lacaios, 
	enquanto o Apocalipse Mental faz você pensar que está com a 
	vantagem nas mãos! Falando em mãos, Heigan nunca lava as dele.</p>

	<p>Curse of Naxxramas traz um tabuleiro exclusivo para os 
	jogadores de Hearthstone lutarem as batalhas desta aventura 
	— com novos cantos interativos!</p>

	<p>Curse of Naxxramas abre os portões para desafiar os jogadores de Hearthstone
	, uma ala de cada vez.Para quem quiser se aventurar rumo às profundezas de 
