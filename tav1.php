<h2 class="text-center">NEFARIAN VS RAGNAROS</h2>

<p>Em alguns fóruns, muitos jogadores acreditam que o
 jogo está “desbalanceado”, devido à diferença de cinco
  manas entre os adversários no início da partida.
   Na verdade, cada herói tem as suas particularidades
   e a melhor maneira de vencê-los é conhecer as vantagens 
   e fraquezas do nosso oponente e utilizá-las a nosso favor.</p>

 <img src="nef.jpg" alt="" class="img-responsive center-block" width="800"	>
 <h2>NEFARIAN</h2>
 <p>- Poder heróico adiciona um feitiço aleatório na mão com custo zero </p>
 <p>- 30 armadura / 30 vida</p>
 <p>- 5 manas iniciais</p>
 <p>O Nefarian é absurdamente forte no “early game”,
  por isso seu estilo de jogo acaba sendo mais agressivo.
   Sua sinergia com dragões é ótima, e a escolha de cartas
    não é muito problema quando temos fortes opcões que
     respeitam a curva de mana. Por este motivo, o
      mulligan pode variar muito.</p>

<img src="ragi.jpg" alt="" class="img-responsive center-block" width="800">

<h2>RAGNAROS</h2>
<p>- Poder heróico invoca um Furiante de Magma 5/1</p>
<p>- Arma Sulfuras 2/6: Após acabar com a durabilidade da arma, seu poder heróico é trocado para “cause 8 de dano aleatório”</p>
<p>- 1 mana inicial</p>
<p>Ragnaros sem dúvida exige um pouco mais do player, 
porque você deve levar a partida até o “late-game”. 
Você começa com menos mana, mas para contrabalancear 
esta desvantagem, você fica mais forte a cada turno. 
Quanto maior o seu dano sofrido, maiores serão os 
seus lacaios, como exemplo o Golemagg (20/20) e o 
Gigante derretido (8/8), que podem entrar no jogo 
com o custo muito reduzido.</p>