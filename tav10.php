<h2 class="text-center">CHUVA DE MANA</h2>

<p>Mais uma Taverna entrou no ar, e ao contrário do que 
aconteceu nas duas últimas semanas, o tema da vez é inédito: 
“Chuva de Mana”. A nova regra consiste no fato que 
ganhamos cristais de dois a dois: começamos com um 
cristal de mana, depois recebemos 3, 5, 7, 9 e 10.</p>

<p>Também ao contrário do que vinha acontecendo, desta 
vez os decks não são prontos. É importante, portanto, 
pensar em cartas ou combos que se encaixem nas curvas 
da nova regra.</p>

