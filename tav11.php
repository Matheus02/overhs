<h2 class="text-center">SUSPIRADOR SUSPIRANTE</h2>

<p>“Suspirador Suspirante” tem a seguinte regra: “A morte 
não é bem o fim… porque os lacaios com Último Suspiro 
agora suspiram duas vezes.</p>

<p>É importante, nesta semana, apostar em boas cartas 
com Último Suspiro, como Retalhador Guiado, e em 
cartas para parar o oponente, como as que silenciam 
inimigos, caso da Coruja Bico-de-ferro. Algumas jogadas 
muito bacanas, que tem sido usadas em vários decks, 
incluem utilizar Stalagg e Feugen, e outras cartas 
que não se costuma mais usar na ladder. Cartas como 
Arrota-Lodo, Exorcistinha, Ovo Nerubiano, Acumulador 
e Tesouros, Golem Colheiteiro, e Rastejante Assombrado, 
são ótimas escolhas na hora de montar a estratégia.</p>

