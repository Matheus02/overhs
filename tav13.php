<h2 class="text-center">ROBOMBA CONTRA AZUCRINATRON!</h2>

<p>Bom, como vocês já devem ter percebido, a nova Taverna está no ar! Robomba vs. Azucrinatron!</p>

<p>Os decks são pré-montados, e tem cartas de todas as classes, sendo, 
na maioria, Mecanoides. Vi muitos jogadores afirmando que jogar com 
Azucrinatron é mais fácil, afinal, o Poder Heroico dele é mais forte. 
Mas o da Robomba não fica atrás, funcionando muito bem no decorrer do jogo</p>

<p>Jogando com Azucrinatron, o segredo é usar o Poder Heroico 
SEMPRE que possível, preservando seu heroi e os outros lacaios, 
e aproveitando todas as oportunidades de agressivar. Não há 
muito segredo. Jogando com a Robomba, considerando que o Poder 
Heroico é considerado mais fraco (apesar de ser bem útil para 
tirar uns Escudos Divinos), o segredo é lidar rapidamente com 
os lacaios do oponente, não deixando ele ocupar a mesa.</p>