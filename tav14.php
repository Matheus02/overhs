<h2 class="text-center">DISTRIBUIDOR MECÂNICO DE CARDS</h2>

<p>Bom, mais uma semana com Taverna nova no pedaço! Mas a dessa semana causou algumas confusões…</p>

<p>Chamada de “Distribuidor Mecânico de Cards” pode confundir um pouco os jogadores com sua regra, que diz:
Os gnomos da Vila da Gambiarra estão testando um protótipo de card robótico: o Optimotron! No primeiro turno,
 se você comprar um card, ele terá 1 de custo. No segundo, será um card com 2 de custo, e assim por diante.</p>
 <p>No turno 1 compramos uma carta com 1 de custo, no 2 uma de 2, e assim por diante</p>

<p>Tenha uma boa estratégia e vá em frente! É realmente muito mais fácil pensar nas jogadas com a previsão 
do que virá para sua mão naquele turno, mas, sinceramente, não achei essa Taverna das mais divertidas.</p>

