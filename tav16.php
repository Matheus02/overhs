<h2 class="text-center">QUEM É O CHEFE AGORA?</h2>

<p> Dessa vez, os chefões das masmorras querem entrar 
na brincadeira: o poder heroico é aleatório, e de 
algum chefe do modo aventura! Sempre que for usado, 
ele será trocado por outro.</p>

<img src="poder.jpg" alt="" class="img-responsive center-block" width="974">

<p>Os decks devem ser montados, então toda a carga 
de aleatoriedade da vez está em qual poder heroico 
teremos na mão. A possibilidade de criação de decks, 
a partir disso, é infinita! Cartas como a Dama do 
Lago, Campeão de Mukla, e Gigante de Gelo são ótimas 
escolhas para a lista. Mas fiquem atentos! Os efeitos 
do Draco de Gelarra e do Comandante da Guarnição não 
funcionam nesta Taverna! Cartas à parte, é uma ótima 
chance para usar aqueles decks mais divertidos</p>