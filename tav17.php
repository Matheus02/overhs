<h2 class="text-center">UNIDOS CONTRA MECANOZOD!</h2>

<p>o modo cooperativo da Taverna chegou esta semana! 
Depois de muita espera, enfim podemos experimentar 
como é se unir a outro jogador para enfrentar um 
inimigo comum: Mecanozod! Não, você não deve derrotar 
seu amiguinho, muito pelo contrário. Se um heroi 
morrer, é derrota coletiva!</p>

<p>Diferente do que podia parecer, o Mecanozod é um 
inimigo beeem difícil de se enfrentar, não pela sua 
presença na mesa em si (ele não ataca diretamente), 
mas pelos seus feitiços. É muito importante entender 
as ações do inimigo em comum para conseguir vencer 
o jogo. Mecanozod fica a cada turno em um lado do 
tabuleiro, ou seja, na sua vez, ele estará do lado 
de seu aliado (o outro heroi) e vice-versa.</p>

<p>Os decks são pré montados, e as duas classes 
disponíveis são Sacerdote e Paladino, que são 
determinadas aleatoriamente. Acredito que a escolha 
das duas classes se relacione com a quantidade de 
feitiços de cura e buffs para lacaios que elas possuem 
(e esses aspectos são fundamentais para se conseguir 
a vitória esta semana)</p>

