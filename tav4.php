<h2 class="text-center">A GRANDE COMPETIÇÃO DE EVOCADORES</h2>
<p>Dessa vez temos um prato cheio para decks de 
feitiços! E a regra da vez é a seguinte: sempre 
que você lançar um feitiço, um lacaio com mesmo 
custo é evocado diretamente no campo de batalha 
pra você.</p>
<p>Tenham em mente que se você tiver um feitiço 
para jogar, sempre terá lacaios em sua mesa. E 
muitas vezes um feitiço barato pode trazer um 
lacaio interessante</p>
<p>Decks de Druida e Paladino também podem fazer 
um bom trabalho, mas se você tem pelo menos todas 
as cartas básicas de Mago, provavelmente você 
conseguirá a vitória.</p>