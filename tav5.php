<h2 class="text-center">ENCONTRO NA ENCRUZILHADA</h2>
<p>Desta vez não há um tema em especial. O grande destaque 
é a aleatoriedade. Você entra no jogo, escolhe um heroi, e 
um deck completamente inesperado é montado pra você. 
Não há como preparar uma estratégia especifica para a partida, 
a não ser escolher um heroi com o qual vocês já estão acostumados a jogar.</p>

<p>São usadas todas as cartas possíveis do jogo, desde as clássicas, 
até as cartas de aventuras. Todas, todas, todas. Existe a possibilidade 
de jogar com um deck completamente básico ou um cheio de lendárias. 
Tudo é completamente imprevisível! Justamente por isso as coisas 
podem não parecer tão boas em um primeiro momento… </p>