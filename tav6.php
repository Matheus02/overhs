<h2 class="text-center">CORAÇÃO DA NASCENTE DO SOL</h2>

<p>Mais uma Taverna entrou ao ar, e vamos combinar que 
está uma loucura: começamos o jogo já com 10 de Mana. 
O deck é montado pelos jogadores.</p>

<p>Mas aí muitos já devem pensar: “ÓTIMO! Coloco zilhões 
de lacaios caros e ganho o jogo!”. Talvez não ganhe. Muito 
mais importante do que as cartas mais fortes do jogo, é 
importante pensar na sinergia do deck. Não adianta colocar 
um lacaio parrudo no primeiro turno se ele não conseguir 
segurar o seu adversário, certo? Além dos mais, lacaios 
bons não trabalham sozinhos, trabalham em conjunto. 
Também não é necessário usar toda a sua mana em um turno 
só, planeje suas jogadas de acordo com o que vai acontecendo.</p>