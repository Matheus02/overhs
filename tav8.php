<h2 class="text-center">O BAILE DE MÁSCARAS</h2>

<p>A regra da semana é a seguinte: todos os lacaios possuem o 
Último Suspiro, ou seja, sempre que um lacaio for morto, um 
que custa dois cristais de mana a menos é evocado no seu 
lugar. E desta vez, ao contrário do que já vinha acontecendo 
há algumas semanas, o jogador deve montar o deck.</p>

<p>Algumas pessoas consideram essa uma das Tavernas mais 
difíceis, se não for a mais difícil delas. Mas o fato é 
que as trocas se tornaram o centro do jogo e devem ser 
bem analisadas. Nota-se também que feitiços para remoção 
não são lá muito eficientes, afinal são dois lacaios 
para remover. Ou seja, o jogador deve ter em mente que 
o deck (principalmente nos casos de Magos, Sacerdotes, 
e outras classes que usam muitos feitiços) deve ser
focado em lacaios.</p>