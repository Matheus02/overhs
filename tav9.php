<h2 class="text-center">DUELO DO GRANDE TORNEIO</h2>

<p>A taverna dessa semana é com duas classes com decks 
pré montados do Grande Torneio – Mago e Caçador. Já dá 
pro pessoal ver como roda algumas cartas, quais são 
legais e quais estão com seu encaixe meio maluco ainda.</p>

<p>O que podemos ver com as novas cartas, principalmente 
com o caçador, é que o deck pré montado dessa semana não 
roda como agressivo, se você sair  com o caçador pra jogar 
nessa contenda, não vá face. Controle o campo e não deixe 
o mago ficar com lacaios “parrudos”, principalmente aqueles 
com efeito de inspirar.</p>

