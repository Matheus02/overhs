<h2 class="text-center">O GRANDE TORNEIO</h2>

<p>Hearthstone: Heroes of Warcraft é o game digital inspirado no 
universo de Warcraft lançado pela Blizzard para PCs, tablets e 
smartphones. Com estratégias profundas e grande variedade de 
cards, o jogo acaba de receber a expansão O Grande Torneio, 
que adiciona mais de 300 novas cartas ao título.</p>

<img class="img-responsive center-block" src="tgt.jpg" alt="" width="750" height="400">

		<h3 class="text-center">NOVAS CARTAS</h3>

<p>O Grande Torneio traz 132 novos cards para sua coleção! 
São feitiços e armas novas junto com os mais famosos campeões
 de Azeroth para você lutar pela sua causa. Continue lendo
  e sinta um gostinho do que espera você no Grande Torneio!</p>

  		<h3 class="text-center">INSPIRE-SE</h3>

  	<p>O Grande Torneio é o palco ideal para a competição
  	 amistosa entre cavaleiros peritos. "Inspirar", a nova
  	  palavra-chave dos lacaios, se ativa quando o seu 
  	  Poder Heroico é usado. Usar o seu Poder Heroico
  	   quando um lacaio com Inspirar está na mesa resulta
  	    em alguns efeitos especiais.</p>

  	 <h3 class="text-center">UM TORNEIO LENDÁRIO</h3>  

  	<p>Os cards do Grande Torneio para Windows ®, Mac®, iOS®
  	 e Android® estarão disponíveis para compra com ouro ou
  	  dinheiro real a partir de agosto. Os pacotes de cards
  	   do Grande Torneio vão seguir a estrutura de preços atual.</p>

	<p>Os cards do Grande Torneio estarão disponíveis na Arena
	 no dia do lançamento, mesmo que você não tenha adicionado
	  os cards à sua coleção.</p>

	  <h3 class="text-center">UM NOVO TABULEIRO</h3>  

	<img class="img-responsive center-block" src="tabtgt.jpg" alt="" width="750" height="400">

	<p>O Grande Torneio traz um novo tabuleiro para lutar. Nele,
	 você poderá encontrar tudo o que precisa para uma agradável experiência no torneio.!</p>

	 <h3 class="text-center">RECOMPENSA DA ARENA</h3>

	 <p>A partir do começo do Grande Torneio, a Arena não vai mais recompensar
	  os jogadores apenas com pacotes de cards de Goblins vs Gnomos. Em vez 
	  disso, eles terão chance de receber pacotes de card de Goblins vs Gnomos,
	   do Grande Torneio ou do pacote de cards Clássico ao receber um pacote de recompensa.</p>