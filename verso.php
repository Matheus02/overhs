<p>Esse verso é adquirido completando uma missão em um Encontro Fireside.</p>
<p>Fireside é um tipo de evento criado pela Blizzard para promover a 
socialização entre os jogadores. São reuniões presenciais 
com outros jogadores. Para receber esse verso, basta você jogar três 
partidas em um desses eventos (independente de ganhar ou perder), 
desde que tenha 3 ou mais jogadores conectados simultaneamente no 
Hearthstone na mesma rede. Cada partida deve ser jogada contra um 
jogador que esteja jogando na mesma subrede que você.</p>


