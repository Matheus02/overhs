<h2>O QUE SÃO VERSOS DE CARTAS?</h2>


<p>Versos de cartas são um recurso estético que trazem 
a possibilidade de modificar a aparência das suas cartas, 
deixando-as mais personalizadas.</p>
<img class="img-responsive center-block" src="mesa.png" alt="">

<h2>COMO POSSO MODIFICAR MEU VERSO ATUAL?</h2>

<p>Para alterar o verso das suas cartas, basta acessar o 
Menu do Jogo, Minha Coleção e Clicar na estrelinha acima 
da coleção.</p>
<img class="img-responsive center-block" src="mesa2.png" alt="">
<p>Agora há a opção de escolher um verso de carta para 
cada Deck. Basta Clicar com o BOTÃO DIREITO sobre a 
carta que ela será selecionada para o deck escolhido.</p>

<h2>COMO RECEBO ESSES VERSOS DE CARTAS?</h2>

<p>Uma das formas mais comuns de adquirir versos de 
cartas é através das temporadas mensais. Ao atingir 
rank 20 em uma temporada, você estará apto para 
receber o verso da temporada no final do mês. As 
temporadas em geral, se iniciam no dia 1º de cada 
mês e terminam no último dia do mês. Cada mês possui 
uma temática diferente. Se você atingir rank 20 
durante o mês, NÃO SE PREOCUPE, você receberá o 
verso, mas eles só são entregues no final do mês. 
Para atingir rank 20, basta jogar no modo ranqueado</p>

